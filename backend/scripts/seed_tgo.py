# SEED FUNCTION
# ============================================================================


def seed_tgo_questions():
    db = SessionLocal()
    try:
        # Ensure subject exists
        tgo_subject = (
            db.query(DBSubject).filter(DBSubject.id == SubjectId.TGO.value).first()
        )
        if not tgo_subject:
            tgo_subject = DBSubject(
                id=SubjectId.TGO.value, name="Оқуға дайындығын анықтау тесті (TGO)"
            )
            db.add(tgo_subject)
            db.commit()

        # Clear existing TGO questions
        db.query(DBQuestion).filter(
            DBQuestion.subject_id == SubjectId.TGO.value
        ).delete()
        db.commit()

        all_questions = []

        # Add Critical Thinking questions (base + extra + extra_2 + extra_3)
        for q in (
            CRITICAL_THINKING
            + EXTRA_CRITICAL_THINKING
            + EXTRA_CRITICAL_THINKING_2
            + EXTRA_CRITICAL_THINKING_3
        ):
            all_questions.append({**q, "topic": "critical_thinking"})

        # Add Analytical Thinking questions (base + extra + extra_2 + extra_3)
        for q in (
            ANALYTICAL_THINKING
            + EXTRA_ANALYTICAL_THINKING
            + EXTRA_ANALYTICAL_THINKING_2
            + EXTRA_ANALYTICAL_THINKING_3
        ):
            all_questions.append({**q, "topic": "analytical_thinking"})

        # Validate question count
        a_count = sum(1 for q in all_questions if q.get("difficulty") == "A")
        b_count = sum(1 for q in all_questions if q.get("difficulty") == "B")
        c_count = sum(1 for q in all_questions if q.get("difficulty") == "C")

        print(f"Seeding {len(all_questions)} TGO questions...")
        print(f"  Қиындық бөлінуі: A={a_count}, B={b_count}, C={c_count}")

        for q_data in all_questions:
            q_id = str(uuid.uuid4())

            # Handle chart data - store as JSON in reading_passage with CHART: prefix
            reading_passage = None
            if q_data.get("chart"):
                reading_passage = "CHART:" + json.dumps(
                    q_data["chart"], ensure_ascii=False
                )

            db_question = DBQuestion(
                id=q_id,
                subject_id=SubjectId.TGO.value,
                text=q_data["text"],
                type=QuestionType.SINGLE,
                topic=q_data["topic"],
                difficulty=q_data.get("difficulty", "B"),
                reading_passage=reading_passage,
            )
            db.add(db_question)

            correct_ids = []
            for idx, opt_text in enumerate(q_data["options"]):
                opt_id = str(uuid.uuid4())
                db_option = DBOption(id=opt_id, question_id=q_id, text=opt_text)
                db.add(db_option)
                if idx == q_data["correct"]:
                    correct_ids.append(opt_id)

            db_question.correct_option_ids = ",".join(correct_ids)

        db.commit()
        print(f"[SUCCESS] TGO database seeded with {len(all_questions)} questions.")
        print(f"  - Сыни ойлау: {len(CRITICAL_THINKING)} сұрақ")
        print(f"  - Аналитикалық ойлау: {len(ANALYTICAL_THINKING)} сұрақ")

    except Exception as e:
        db.rollback()
        print(f"Error: {e}")
        raise
    finally:
        db.close()


if __name__ == "__main__":
    seed_tgo_questions()
